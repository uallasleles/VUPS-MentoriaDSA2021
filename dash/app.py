# Instale o Dash

# > pip install dash

# Isso traz três bibliotecas de componentes que constituem o núcleo do Dash:

# - dash_html_components
# - dash_core_components
# - dash_table

# bem como a biblioteca de gráficos plotados, 

# - plotly. 

# Essas bibliotecas estão em desenvolvimento ativo, 
# portanto, instale e atualize com frequência.

# ----------------------------------------------------------------------------

# Se você preferir Jupyter notebook ou JupyterLab como seu ambiente de desenvolvimento, 
# recomendamos instalar o jupyter-dash:

# > pip install jupyter-dash

# Também recomendamos a instalação do Pandas, que é exigido pelo Plotly Express e 
# usado em muitos de nossos exemplos.

# > pip install pandas


# Dash Layout
# ============================================================================

# > Para aplicativos Dash de produção, 
# > recomendamos estilizar o layout do aplicativo 
# > com o Dash Enterprise Design Kit.

# Os aplicativos do Dash são compostos de duas partes. 
# 1. A primeira parte é o "layout" do aplicativo e descreve a aparência do aplicativo. 
# 2. A segunda parte descreve a interatividade do aplicativo.

# Dash fornece classes Python para todos os componentes visuais do aplicativo. 
# Mantemos um Conjunto de Componentes nas bibliotecas 
# - dash_core_components e 
# - dash_html_components, 
# mas você também pode construir o seu próprio com 
# - JavaScript e 
# - React.js.

